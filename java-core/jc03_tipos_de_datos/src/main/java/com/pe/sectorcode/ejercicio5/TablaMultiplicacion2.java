package com.pe.sectorcode.ejercicio5;

public class TablaMultiplicacion2 {

	public static void main(String[] args) 
	{

			int tabla    = 9;
			
			for (int contador = 0; contador <= 12; contador++)
			{
				System.out.println (tabla + " x "+ contador + " = "+ (tabla*contador) );
			}
	}

}
